#include "utils.h"

int			char_to_num(char *line, int *num)
{
	int		i;

	i = 0;
	*num = 0;
	while (ft_isalnum(line[i]) == 4)
	{
		*num = (*num * 10) + line[i] - '0';
		i++;
	}
	return (i);
}
