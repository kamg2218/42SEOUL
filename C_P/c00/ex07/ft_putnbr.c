#include <unistd.h>

void	ft_putnbr(int nb)
{
	int c;

	if (nb < 0)
	{
		write(1, "-", 1);
		if (nb == -2147483648)
		{
			nb = -nb - 8;
			ft_putnbr(nb / 10);
			write(1, "8", 1);
			return ;
		}
		else
			nb = -nb;
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		c = '0' + nb % 10;
	}
	else
		c = '0' + nb;
	write(1, &c, 1);
}
