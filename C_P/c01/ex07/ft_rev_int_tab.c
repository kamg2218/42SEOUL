void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int temp;

	i = size / 2;
	while (i-- > 0)
	{
		temp = tab[size - i - 1];
		tab[size - i - 1] = tab[i];
		tab[i] = temp;
	}
}
